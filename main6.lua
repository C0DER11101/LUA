-- getting minimum and maximum value from a list of numbers

print("Minimum value: "..math.min(10, 1, 50, 12, 19, 22, 46))
print("Maximum value: "..math.max(10, 1, 50, 12, 19, 22, 46))

-- floor()
print(math.floor(3.14159))   -- rounds down, will print 3

-- ceil()
print(math.ceil(3.14159))    -- rounds up, will print 4
